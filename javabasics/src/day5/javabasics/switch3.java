package day5.javabasics;

public class switch3 {
  public static void main(String[]args)
{
	int month=3;
	switch(month)
	{
	case 1:
		System.out.println("minth of january");
		break;
	case 2:
		System.out.println("minth of feb");
		break;
	case 3:
		System.out.println("minth of march");
		break;
	case 4:
		System.out.println("minth of april");
		break;
	default:
		System.out.println("sorry no match found for the caseor value u have entered is invalid");
		break;
	}
	System.out.println("program ends");
	
	}
	  

}
